export const getPackageWeight = (code: string, name: string, unit: string): number => {
    if (MATERIAL_WEIGHTS_DB[code]) return MATERIAL_WEIGHTS_DB[code];
    const match = name.match(/(\d+(\.\d+)?)\s*(كجم|لتر|جرام|جم)/);
    if (match) {
        let val = parseFloat(match[1]);
        if (name.includes('جرام') || name.includes('جم')) return val * 0.001;
        return val;
    }
    const defaults: Record<string, number> = { 'شيكارة': 25, 'جركن': 20, 'برميل': 200, 'عبوة': 5, 'كيس': 1, 'لتر': 1, 'كجم': 1, 'بستلة': 20, 'اسطوانة': 50 };
    return defaults[unit] || 1;
};
